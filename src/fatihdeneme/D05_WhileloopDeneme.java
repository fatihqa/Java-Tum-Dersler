package fatihdeneme;

public class D05_WhileloopDeneme {

    // 2 basamakli sayilari aralarinda bir bosluk olacak sekilde yanyana yazdirin
    public static void main(String[] args) {

        for (int i = 10; i <=99; i++) {
            System.out.print(i+ " ");

        }

        int a =10;
        while (a<100){
            System.out.print(a+ " ");
            a++;

        }
    }


}
